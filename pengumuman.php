            </li>
            <li>
                <strong>Waktu Pendaftaran:</strong> Waktu Penerimaan Pendaftaran Bakal Calon Dukuh Salam selama 7 hari kerja mulai tanggal 05 s/d 16 Februari 2024, dengan pengaturan jam kerja sebagai berikut:
