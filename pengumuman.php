    <h2>Kelengkapan Persyaratan Administrasi</h2>
        <p>
            Kelengkapan persyaratan administrasi sebagaimana dimaksud disampaikan kepada Panitia Pengisian Pamong Kalurahan dalam bentuk soft copy dan hard copy (yang keduanya sama) dan lengkap paling lambat pada hari terakhir pendaftaran bakal calon Pamong Kalurahan jam 15.00 WIB sesuai jam yang terpasang di Sekretariat Panitia Pengisian Pamong Kalurahan.
        </p>

            <p><strong>Catatan:</strong></p>
            <ul>
                <li>
                    Format Surat Permohonan, Daftar Riwayat Hidup, dan Surat Pernyataan sebagai persyaratan administrasi, dapat diunduh di Website Pemerintah Kalurahan Temuwuh: 
                    <a href="https://temuwuh.bantulkab.go.id/" target="_blank">https://temuwuh.bantulkab.go.id/</a>
                </li>
                <li>
                    Berkas bentuk softcopy adalah berkas hardcopy yang discan dengan format PDF dan dijadikan dalam 1 (satu) folder, kemudian dikirim via email Panitia Pengisian Pamong Kalurahan, Kalurahan Temuwuh dengan alamat email: 
                    <a href="mailto:tppkaltemuwuh@gmail.com">tppkaltemuwuh@gmail.com</a>
                </li>
                <li>
                    Berkas hardcopy dimasukkan dalam stopmap dengan warna sesuai formasi Jabatan Dukuh Salam, yaitu stopmap warna hijau.
                </li>
            </ul>

