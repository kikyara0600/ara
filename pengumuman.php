        <p>HALLO Pemuda Pemudi Kalurahan Temuwuh...</p>
        <p>Mari berjuang besama-sama membangun Kalurahan Temuwuh dengan mengikuti pendaftaran dan ujian seleksi Pamong Kalurahan Temuwuh formasi jabatan Dukuh Salam . Berikut informasinya, check it out:</p><br>
        <p><strong><center>PENGISIAN LOWONGAN PAMONG KALURAHAN</center></strong></p>
        <p><strong><center>KALURAHAN TEMUWUH, KAPANEWON DLINGO, KABUPATEN BANTUL</center></strong></p>
        <p><strong><center>TAHUN 2024</center></strong></p>
        
        <p>Pendaftaran Bakal Calon Pamong Kalurahan Jabatan Dukuh Salam, Kalurahan Temuwuh, Kapanewon Dlingo, Kabupaten Bantul, beserta seleksi/penelitian administrasi, dibuka mulai tanggal 05 s/d 16 Februari 2024, bertempat di Sekretariat Panitia Pengisian Pamong Kalurahan, yaitu di Kantor Kalurahan Temuwuh, Jalan Raya Patuk - Dlingo Km 12, Temuwuh, Dlingo.</p>
        
        <h2>Persyaratan Umum Bakal Calon Pamong Kalurahan:</h2>
        <ul>
            <li>berpendidikan paling rendah Sekolah Menengah Atas atau yang sederajat;</li>
            <li>berusia paling rendah 20 (dua puluh) tahun dan paling tinggi 42 (empat puluh dua) tahun pada tanggal akhir pendaftaran;</li>
            <li>terdaftar sebagai Warga Negara Indonesia; dan</li>
            <li>memenuhi kelengkapan persyaratan.</li>
        </ul>
        
        <h2>Persyaratan Khusus Bakal Calon Pamong Kalurahan:</h2>
        <ul>
            <li>sanggup bekerja penuh waktu sebagai Pamong Kalurahan;</li>
            <li>sanggup bekerja sama dengan Lurah;</li>
            <li>sanggup tidak mengundurkan diri sebagai Pamong Kalurahan paling sedikit 5 (lima) tahun sejak dilantik sebagai Pamong;</li>
            <li>tidak pernah terlibat penyalahgunaan narkotika dan obat-obat terlarang lainnya (Narkoba);</li>
            <li>bersedia mengundurkan diri sebagai pengurus partai politik ketika dilantik sebagai Pamong Kalurahan;</li>
            <li>bersedia memberikan pelayanan kepada masyarakat sesuai dengan tugas dan fungsinya sebagai Pamong Kalurahan Jabatan Dukuh Salam;</li>
            <li>mendapatkan izin dari Pejabat Pembina Kepegawaian, bagi Calon Pamong Kalurahan yang berasal dari Aparatur Sipil Negara;</li>
            <li>mendapatkan ijin dari Lurah, bagi Calon Pamong Kalurahan yang berasal dari Pamong Kalurahan, Staf Pamong Kalurahan, atau Staf Honorer Kalurahan;</li>
            <li>bersedia mengundurkan diri dari keanggotaan Badan Permusyawaratan Kalurahan apabila terpilih sebagai Pamong Kalurahan, bagi calon Pamong Kalurahan yang berasal dari Badan Permusyawaratan Kalurahan;</li>
            <li>memperoleh dukungan dari penduduk Padukuhan setempat, setempat untuk bakal calon Pamong Kalurahan jabatan Dukuh Salam;</li>
            <li>bersedia menjadi penduduk dan bertempat tinggal di Padukuhan setempat dibuktikan dengan Kartu Tanda Penduduk sejak diangkat menjadi Pamong Kalurahan jabatan Dukuh Salam.</li>
        </ul>
        
