        <h1 style="color:#dbaf6c ;" ><CENTER>Visi dan Misi Pemerintah Desa Temuwuh</CENTER></h1>
        <h2 style="color:#dbaf6c ;">Visi</h2>
        <p>Visi adalah suatu gambaran yang menantang tentang keadaan masa depan yang diinginkan dengan 
            melihat potensi dan kebutuhan Kalurahan, sedangkan Misi adalah langkah strategis untuk mencapai visi 
            atau rumusan umum yang berbicara mengenai upaya-upaya, pada implementasinya diwujudkan dalam beberapa 
            aspek diantaranya meliputi :</p>
            <ul>
                <li>Pertama, Aspek Perencanaan, didalam UU Desa pasal 78 dan 79 mensyaratkan keharusan bagi Pemerintah Kalurahan/Lurah untuk melaksanakan Perencanaan Pembangunan Kalurahan dalam rangka menyusun visi bersama membangun Kalurahan antara Masyarakat dan Pemerintahan Kalurahan yang diselaraskan dengan rencana pembangunan Kabupaten/Kota kemudian dituangkan dalam Dokumen Jangka menengah (RPJM Desa) dan Rencana Kerja Pemerintah Desa (RKP Desa) yang ditetapkan dengan Peraturan Desa.</li>
                <li>Kedua, Aspek Penganggaran, Perencanaan dan penganggaran Keuangan Kalurahan merupakan proses yang terintegrasi sehingga output dari perencanaan keuangan adalah penganggaran. Proses perencanaan arah dan kebijakan pembangunan Kalurahan tahunan dan rencana anggaran tahunan (APBDes) pada hakikatnya merupakan perencanaan instrumen kebijakan publik sebagai upaya meningkatkan pelayanan kepada masyarakat. Disamping itu UU Desa juga memberi jaminan yang lebih pasti bahwa setiap Kalurahan akan menerima dana dari pemerintah melalui anggaran negara dan daerah yang jumlahnya berlipat, hal ini merupakan modal finansial bagi Kalurahan dalam upaya mewujudkan visinya.</li>
                <li>Ketiga, Aspek Pelaksanaan, Pasal 81 UU Desa menyatakan bahwa pembangunan Kalurahan dilaksanakan sesuai dengan Rencana Kerja Pemerintah Desa dan dilaksanakan oleh Pemerintah Kalurahan dengan melibatkan seluruh masyarakat Kalurahan dengan semangat gotong royong serta memanfaatkan kearifan lokal dan sumber daya alam Kalurahan. Pada ayat (4) pasal 81 ini ditegaskan bahwa pembangunan lokal berskala Kalurahan dilaksanakan sendiri oleh Kalurahan. Sedangkan pelaksanaan program sektoral yang masuk ke Kalurahan diinformasikan kepada Pemerintah Kalurahan untuk diintegrasikan dengan Pembangunan Kalurahan. Partisipasi, gotong royong, dan peran aktif masyarakat menjadi kata kunci dalam upaya mencapai visi, dan hal ini memerlukan sentuhan seni kepemimpinan kades untuk menggalang dukungan dari masyarakat tersebut.</li>
                <li>Keempat, Aspek Pelaporan dan Pertanggungjawaban,Sejalan dengan prinsip transparansi, akuntabel, dan partisipatif yang merupakan ciri dasar tata kelola pemerintahan yang baik (Good Governance), maka progres kegiatan, capai target, dalam upaya menjalankan visi dan misi Kalurahan perlu dilaporkan dan dipertanggungjawabkan kepada masyarakat. Pertanggungjawaban tidak hanya disampaikan kepada pemerintah yang berwenang, tetapi juga harus disampaikan kepada masyarakat baik langsung maupun tidak langsung. Secara langsung, pertanggungjawaban kepada masyarakat dapat dilakukan melalui musyawarah Kalurahan sebagai forum untuk membahas hal-hal strategis, yang dihadiri BPD dan unsur-unsur masyarakat lainnya.</li>
            </ul>
            <p>Perumusan visi pada umumnya menjawab pertanyaan tentang akan menjadi apa suatu organisasi dan lembaga (what do we want to become?). Visi ini merupakan abstraksi dari bentuk akhir suatu cita-cita yang diupayakan untuk diwujudkan. Adapun yang menjadi Visi Kalurahan Temuwuh adalah :</p>
<p><strong><center>“Menciptakan Tata Kelola Pemerintahan yang Amanah, Jujur dan Bertanggung Jawab dalam Rangka mewujudkan masyarakat Desa Temuwuh yang Transparan, Adil, Makmur,  Aman, Mandiri, dan bertaqwa kepada Tuhan Yang Maha Esa.”</center></strong></p>
<h2 style="color:#dbaf6c ;">Misi</h2>
        <p>Misi adalah langkah strategis untuk mencapai visi atau rumusan umum yang berbicara mengenai upaya-upaya, untuk mewujudkan Visi Kalurahan Temuwuh maka ada beberapa misi yang menjadi acuan, yaitu sebagai berikut :</p>
            <ul>
                <li>Melakukan reformasi sistem kinerja aparatur pemerintahan Kalurahan guna meningkatkan kualitas pelayanan kepada masyarakat.</li>
                <li>Penertiban aset Kalurahan Temuwuh dan memanfaatkannya untuk kepentingan masyarakat.</li>
                <li>Menyelenggarakan urusan pemerintahan Kalurahan secara yang bersih, bebas dari korupsi, terbuka dan bertanggungjawab sesuai dengan peraturan perundang-undangan.</li>
                <li>Meningkatkan perekonomian masyarakat melalui pembentukan kelompok UKM yang produktif  sesuai dengan potensi Kalurahan dan peluang pasar.</li>
                <li>Menjalin kerja sama dengan semua pihak, menyelenggarakan pelatihan dan penyuluhan dalam bidang apapun dalam rangka meningkatkan sumber daya masnusia yang berkualitas.</li>
                <li>Membentuk Badan Usaha Milik Desa (BUMDes) sebagai lembaga yang mengelola produktivitas usaha masyarakat.</li>
                <li>Membangun pola kehidupan masyarakat menjadi masyarakat yang sehat melalui peningkatan kualitas kegiatan posyandu balita, posyandu lansia, kebersihan lingkungan dan pembangunan rumah layak huni berdasarkan skala prioritas.</li>
                <li>Membentuk dan membangun kembali organisasi-organisasi masyarakat Kalurahan Temuwuh untuk membantu pemerintah Kalurahan dalam penyelenggaraan Pemerintahan atas dasar kepentingan masyarakat pada umumnya.</li>
                <li>Membangun relasi dengan lembaga pendidikan formal pada lingkup internal dan eksternal guna menunjang mutu pendidikan generasi Kalurahan Temuwuh untuk menjadi individu yang bertaqwa kepada Allah SWT., bermoral, berpengetahuuan luas, dan mandiri.</li>
                <li>Pembangunan, perbaikan, dan pemeliharaan infrastruktur dalam Kalurahan Temuwuh berdasarkan skala prioritas kebutuhan masyarakat.</li>
            </ul>
            <h2 style="color:#dbaf6c ;">Nilai-nilai</h2>
            <p>Nilai – nilai utama yang melandasi jalannya roda kehidupan pemerintahan, kemasyarakatan dan pembangunan adalah :</p>
            <ul>
                <p> <b>Kebersamaan (partisipatif )</b></p>
                <li>Kebersamaan ini dimaksudkan kerukunan dan kekompakan masyarakat dalam mewujudkan impian bersama dengan kemampuan semaksimal mungkin. Kebersamaan dalam setiap aspek pembangunan. Kebersamaan mulai dari perencanaan, pelaksanaan, maupun pengawasan dan pertanggungjawaban.</li>
                <p><b>Keterbukaan (transparansi)</b></p>
                <li>Jika kebersamaan sudah terlaksana, maka keterbukaan akan mudah dibangun. Keterbukaan mengandung makna bahwa mudahnya masyarakat mengakses segala bentuk kegiatan baik itu pemerintahan, pembangunan, maupun pemerintahan.</li>
                <p><b>Ketepatan (akurasi)</b></p>
                <li>Ketepatan adalah bagian dari kesuksesan. Tepat mengandung pengertian tepat rencana, tepat pelaksanaan, tepat sasaran, tepat pemilihan orang, dan tepat pertanggungjawaban. Ketepatan dalam mengambil keputusan juga merupakan awal dari kesuksesan.</li>
                <p><b>Keberlanjutan</b>(sustainebel)</p>
                <li>Sehebat apapun program, kalau tidak ada keberlanjutannya percuma. Oleh karena perencanaan suatu kegiatan harus disesuaikan dengan apa kegiatan sebelumnya, dan kegiatan apa sesudahnya.</li>
                <p><b>Tanggungjawab</b>(akuntabel)</p>
                <li>Tanggungjawab adalah kata kunci dari setiap pelaksana kegiatan. Tanggungjawab pelaksana kegiatan adalah keterjaminan bahwa kegiatan akan dilaksanakan sesuai rencana yang ditetapkan. Tanggungjawab pelaksana kegiatan juga berarti kemampuan memberikan laporan sesuai aturan.</li>
            </ul>
