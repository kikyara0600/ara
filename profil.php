        <h2 style="color:#dbaf6c ;">Kondisi Lingkungan dan Tata Guna Lahan</h2>
            <p>Luasan lahan non permukiman masih sangat luas, penggunaan lahan sawah adalah 83 Ha, sedangkan lahan tegalan 236 ha, perkebunan 65 ha, hutan lindung 92 ha. Sedangkan dari pemetaan swadaya luasan lahan untuk permukiman hanya sekitar 182 ha. sehingga dari hal tersebut dapat disimpulkan bahwa kondisi lingkungan dan penataan fungsi lahan tidak mengganggu aktifitas permukiman namun perlu diantisipasi adanya masalah yang timbul karena semakin lama terjadi peningkatan jumlah penduduk yang mengganggu tata guna lahan. Sumber daya alam yang ada di desa Temuwuh diantaranya adalah : adanya sumber air, potensi lahan sawah yang tersebar di beberapa dusun, potensi tegalan yang cukup luas dan ladang rumput yang cocok untuk peternakan dan kebun yang sangat luas ditandai dengan warna hijau, selain itu juga terdapat potensi batu kapur dan kayu keras. Besarnya potensi batu kapur belum terukur. Karena sebagian besar wilayah desa Temuwuh merupakan bebatuan kapur. Luasnya lahan kebun, rumput, tegalan dan semak belukar biasanya ditanami dengan tanaman keras yaitu Jati, Mahoni, Akasia dan Sono keling. Potensi kayu lainnya yang tidak tergolong kayu keras adalah kayu Sengon/albasia.

</p>
        


