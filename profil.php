                    <h3>Geologi</h3>
                    <p>Wilayah Desa Temuwuh merupakan daerah dataran tinggi dengan jenis tanah hitam yang  pecah- pecah pada musim kemarau dan lengket pada musim penghujan. Jenis tanahnya kurang subur yang disebabkan karena pori-porinya besar dan mengandung kapur sehingga kurangnya cadangan air namun masih dapat dimanfaatkan warga di bidang pertanian. Potensi geologi yang ada yaitu bahan tambang galian batu putih Jenis batuan ini dimanfaatkan untuk membuat pondasi rumah namun jumlahnya tidak terlalu banyak. </p>
