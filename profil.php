        <h2 style="color:#dbaf6c ;text-align: center;">Kondisi Sosial Ekonomi dan Budaya</h2>

        <div class="sidebar-container">
            <div class="sidebar-item">
                <div class="sidebar-content">
                    <h3>Sosial</h3>
                    <p>Kondisi sosial masyarakat masih sangat kental dengan adanya semangat kebersamaan, kepedulian, dan gotong royong. Meskipun jarak antar wilayah atau dusun berjauhan, namun antar individu dalam lingkup satu dusun hingga desa masih saling mengenal. Dari tabel kegiatan sosial dibawah dapat diketahui beberapa jenis kegiatan sosial yang sering dilakukan oleh masyarakat Temuwuh. Hampir di setiap dusun memiliki kegiatan sosial yang banyak. Dengan adanya kegiatan sosial yang sangat tinggi ini menjadi ciri khas masyarakat Temuwuh untuk saling bantu membantu dalam kegiatan sosial kemasyarakatan.</p>
                </div>
            </div>

                <div class="sidebar-item">
                <div class="sidebar-content">
                    <h3>Ekonomi</h3>
                    <p>Mata pencaharian masyarakat desa Temuwuh adalah di bidang pertanian, peternakan, perikanan, perdagangan, kerajinan, industri pengolahan, dan jasa. Jenis pertanian yang dilaksanakan di desa Temuwuh adalah pertanian di sawah dan tegalan. Tanaman pertanian untuk lahan sawah adalah padi, sedangkan untuk tegalan sebagian besar pada musim penghujan ditanami dengan jagung, ketela dan kacang. Jenis peternakan antara lain sapi, kambing, dan ayam. Jenis perikanan adalah lele. Jenis kerajinan adalah kerajinan kayu, kayu putih, bunga kering dan bunga ronce yang pemasarannya ke kota Yogyakarta bahkan sampai keluar  propinsi . Jenis industri rumah tangga adalah tempe kedelai, dikarenakan terkendala modal dan bahan baku pemasaraan hanya untuk daerah sekitar. Dan jenis jasa adalah jasa menjahit dan beberapa usaha dibidang transportasi.</p>
                </div>
                </div>

                <div class="sidebar-item">
                <div class="sidebar-content">
                    <h3>Budaya</h3>
                    <p>Kegiatan kegiatan pelestarian budaya tetap terjaga meskipun tantangan perubahan jaman semakin besar. Hampir di setiap dusun memiliki kegiatan budaya yang dilaksanakan rutin setiap tahun maupun setiap bulan. Beberapa jenis kegiatan budaya contohnya merti dusun, syawalan, pertemuan trah, pengajian pedukuhan, upacara adat, kenduri. Kegiatan merti dusun diyakini masyarakat dapat memberikan ketentraman bagi masyarakat dalam tingkat dusun. Syawalan dilaksanakan setiap tahun yaitu pada bulan syawal. Pertemuan trah merupakan pertemuan kerabat dekat untuk meningkatkan silaturahmi. Upacara adat contohnya upacara pernikahan, khitanan dll. Dan kenduri merupakan kegiatan masyarakat untuk mendoakan yang mempunyai hajat dengan cara berdzikir bersama.</p>
