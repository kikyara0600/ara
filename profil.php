    <h2 style="color:#dbaf6c ;text-align: center;">Kondisi Fisik Wilayah</h2>


    <div class="sidebar-container">
            <div class="sidebar-item">
                <div class="sidebar-content">
                    <h3>Topografi</h3>
                    <p>Desa Temuwuh terletak di daerah perbukitan dengan ketinggian yang hampir sama antar dusun dan antar wilayah. Desa ini berada pada ketinggian antara 200 s/d 250 dpl. Kondisi kemiringan tanah antara 200 s/d  450Â­Â­. Beberapa daerah yang sangat curam terdapat di sisi barat dan timur, dikarenakan wilayah ini berbatasan dengan sungai, sedang daerah tengah merupakan perbukitan yang landai.</p>
                </div>
            </div>

                <div class="sidebar-item">
                <div class="sidebar-content">
                    <h3>Hidrologi</h3>
                    <p>Dari keadaan topografi Desa Temuwuh yang landai menyebabkan sedikitnya sungai dan sumber air. Sehingga kandungan air tanah sangat sedikit, Sumber air yang adapun merupakan sumber air yang hidup jika musim hujan sedang saat musim kemarau sumber air tersebut mati.  Hal ini sangatlah dirasakan masyarakat apabila terjadi musim kemarau yang panjang, sebagian besar sumber-sumber air kering dan sumur sumur dalam juga mengalami kekurangan air. Pemenuhan kebutuhan air ini sering mengalami kekurangan air baik air untuk kebutuhan air minum maupun air untuk lahan pertanian. Pemenuhan air untuk kebutuhan sehari-hari sebagian besar masyarakat Desa Temuwuh adalah dari sumur-sumur gali dengan kedalaman relatif sangat dalam + 20m. Pada musim kemarau sebagian besar wilayah Temuwuh mulai kekurangan air yaitu pada bulan kedua. Untuk memenuhi kebutuhan air pada musim kemarau, masyarakat mencari ke sumber-sumber air yang terletak di daerah yang rendah dan letaknya sangat jauh dari permukiman penduduk dan dengan cara pembelian air dengan mobil-mobil tangki. Sulitnya pemenuhan air, ini menyebabkan pola pertanian yang ada di Desa Temuwuh adalah pertanian lahan kering dengan 1 kali panen tiap tahunnya. Melihat kondisi ini pemenuhan air sangat penting dan mendesak sehingga menjadi prioritas pembangunan desa Temuwuh karena pemenuhan air bersih sangat berdampak dengan sanitasi, kesehatan, dan ekonomi masyarakat.</p>
                </div>
                </div>

                <div class="sidebar-item">
